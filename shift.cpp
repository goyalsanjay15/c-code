#include <iostream>
using namespace std;
int main(){
    
 int x=10;

// Right shift 
/*Calculated  by dividing by 2 the number of times equal to number of shifts*/

// gives remainder after dividing 
 x>>=2;
 cout << x<< endl;


int y=10;
// Left shift
 /* Calculated as 2's complement*/

 // multiply by 2
 y<<=2;
 cout << y;


}
